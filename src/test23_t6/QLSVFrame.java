    private void tblSvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSvMouseClicked
        // TODO add your handling code here:

        int row = this.tblSv.getSelectedRow();
        if (row == -1) {
            return;
        }
        String ten = this.tblSv.getValueAt(row, 0).toString();
        String ma = this.tblSv.getValueAt(row, 1).toString();
        String danhmuc = this.tblSv.getValueAt(row, 2).toString();
        String tinhTrang = this.tblSv.getValueAt(row, 3).toString();
        String donGia = this.tblSv.getValueAt(row, 4).toString();

        this.txtname.setText(ten);
        this.txtMa.setText(ma);
        this.cbbdanhmuc.setSelectedItem(danhmuc);
        if (tinhTrang.equalsIgnoreCase("Còn hàng")) {
            this.rdoCon.setSelected(true);
        } else {
            this.rdoHet.setSelected(true);
        }
        this.txtGia.setText(donGia);
    }//GEN-LAST:event_tblSvMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        ArrayList<SinhVien> ds = this.qlsv.getList();
        SinhVien sv = this.getForm();
        if (sv == null) {
            return;
        }
        this.qlsv.insert(sv);
        this.loadTable();
        this.clearForm();
        JOptionPane.showMessageDialog(this, "Thêm thành công");
         File file = new File(filename);
        FileOutputStream fos;
        try {
            fos = new FileOutputStream(file);
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(this.qlsv.getList());
            JOptionPane.showMessageDialog(this, "Ghi ok");
            oos.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Khong tim thay file");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Loi vao ra");

        }

    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = this.tblSv.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Hãy chọn vào 1 dòng trên table");
            return;
        }
        SinhVien sv = this.getForm();
        this.qlsv.update(row, sv);
        this.loadTable();
        this.clearForm();
        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

