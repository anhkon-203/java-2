    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
        // TODO add your handling code here:\
        File file = new File(filename);
        FileInputStream fis;
        if (file.exists() == false) {
            JOptionPane.showMessageDialog(this, "File 0 ton tai");
            return;
        }
        try {
            fis = new FileInputStream(file);
            ObjectInputStream ois = new ObjectInputStream(fis);
            ArrayList<SinhVien> ds = (ArrayList<SinhVien>) ois.readObject();
            this.qlsv.setList(ds);
            this.loadTable();
            JOptionPane.showMessageDialog(this, "doc ok");
            ois.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Khong tim thay file");
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Loi vao ra");
        } catch (ClassNotFoundException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Loi du lieu");
        }


    }//GEN-LAST:event_btnDocActionPerformed

