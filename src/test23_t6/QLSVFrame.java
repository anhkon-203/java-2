    private void loadTable() {
        ArrayList<SinhVien> ds = this.qlsv.getList();
        DefaultTableModel dtm = (DefaultTableModel) this.tblSv.getModel();
        dtm.setRowCount(0);
        for (SinhVien d : ds) {
            Object[] rowdata = {
                d.getTenSP(),
                d.getMaSp(),
                d.getDanhMuc(),
                d.getTinhTrang() == 1 ? "Còn hàng" : "Hét hàng",
                d.getDonGia()
            };
            dtm.addRow(rowdata);
        }
    }

    private SinhVien getForm() {
        String ten = this.txtname.getText();
        String ma = this.txtMa.getText();
        String danhMuc = (String) this.cbbdanhmuc.getSelectedItem();
        int tinhTrang = this.rdoCon.isSelected() ? 1 : 0;
        String donGia = this.txtGia.getText();

        if (ma.equals("") || ten.equals("") || donGia.equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return null;
        }

        SinhVien sv = new SinhVien(ten, ma, danhMuc, tinhTrang, Double.parseDouble(donGia));
        return sv;

    }

