    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = this.tblSP.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần sửa trên table");
            return;
        }
        sanPham sp = this.getformData();
        if (sp == null) {
            return;
        }

        this.qlsp.update(row, sp);
        this.loadTable();
        this.clear();
        JOptionPane.showMessageDialog(this, "sửa thành công");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = this.tblSP.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Chọn dòng cần sửa trên table");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có muốn xoá bản ghi này ?");

        if (confirm == JOptionPane.NO_OPTION) {
            return;
        } else if (confirm == JOptionPane.CANCEL_OPTION) {
            return;
        } else {

        }
        this.qlsp.delete(row);
        this.loadTable();
        this.clear();
        JOptionPane.showMessageDialog(this, "Xoá thành công");
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSPMouseClicked
        // TODO add your handling code here:
        int row = this.tblSP.getSelectedRow();

        if (row == -1) {
            return;
        }
        String Ten = this.tblSP.getValueAt(row, 0).toString();
        String masp = this.tblSP.getValueAt(row, 1).toString();
        String danhmuc = this.tblSP.getValueAt(row, 2).toString();

        String tinhTrang = this.tblSP.getValueAt(row, 3).toString();
        String donGia = this.tblSP.getValueAt(row, 4).toString();

        this.txtTenSp.setText(Ten);
        this.txtMaSp.setText(masp);
        this.txtTenSp.setText(Ten);
        this.txtDonGia.setText(donGia);

        this.cbbDanhMuc.setSelectedItem(danhmuc);

        if (tinhTrang.equals("Còn hàng")) {
            this.rdoCon.setSelected(true);
        } else {
            this.rdoHet.setSelected(true);
        }

    }//GEN-LAST:event_tblSPMouseClicked

    private void btnDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocActionPerformed
        // TODO add your handling code here:
        File file = new File(Filename);
        if (file.exists() == false) {
            JOptionPane.showMessageDialog(this, "File ko tồn tại");
            return;
        }
        FileInputStream fis;
        try {
            fis = new FileInputStream(file);
            ObjectInputStream ois = new ObjectInputStream(fis);
            ArrayList<sanPham> ds = (ArrayList< sanPham>) ois.readObject();
            JOptionPane.showMessageDialog(this, "Đọc thành công");
            this.qlsp.setList(ds);
            this.loadTable();
            ois.close();
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy file");
            e.printStackTrace();
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Lỗi vào ra");
            e.printStackTrace();
        } catch (ClassNotFoundException e) {
            JOptionPane.showMessageDialog(this, "Lỗi dữ liệu");
            e.printStackTrace();
        }


    }//GEN-LAST:event_btnDocActionPerformed

