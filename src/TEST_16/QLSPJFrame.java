    private void loadTable() {
        ArrayList<sanPham> ds = this.qlsp.getlist();
        DefaultTableModel dtm = (DefaultTableModel) this.tblSP.getModel();
        dtm.setRowCount(0);
        for (sanPham n : ds) {
            Object[] rowData = {
                n.getTenSP(),
                n.getMaSP(),
                n.getDanhMuc(),
                n.getTinhTrang() == 1 ? "Còn hàng" : "hết hàng",
                n.getDonGia()
            };
            dtm.addRow(rowData);
        }
    }

    private sanPham getformData() {
        String tenSp = this.txtTenSp.getText();
        String maSp = this.txtMaSp.getText();
        String danhMuc = (String) this.cbbDanhMuc.getSelectedItem();
        int tinhTrang = this.rdoCon.isSelected() ? 1 : 0;
        String donGia = this.txtDonGia.getText();

        if (txtTenSp.getText().trim().equals("")
                || txtMaSp.getText().trim().equals("")
                || txtDonGia.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return null;
        }
        
        try {
//            int donGia = Integer.parseInt(this.txtDonGia.getText());
    int DonGia = Integer.parseInt(txtDonGia.getText());
            if (DonGia <=0) {
                JOptionPane.showMessageDialog(this, "Đơn giá phải lớn hơn 0");
                return null;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Đơn giá phải là số");
        }

        sanPham sp = new sanPham(tenSp, maSp, danhMuc, tinhTrang, Integer.parseInt(donGia));
        return sp;
    }

